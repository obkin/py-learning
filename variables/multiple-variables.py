# Python allows you to assign values to multiple variables in one line:
x, y, z = "Apple", "Banana", "Cherry"

print(x) # Apple
print(y) # Banana
print(z) # Cherry


# You can assign the same value to multiple variables in one line:
a = b = c = "OneVariable"

print(a) # OneVariable
print(b) # OneVariable
print(c) # OneVariable


# Python allows you to extract values from lists or tuples into variables (This is called unpacking):
fruit = ['apple', 'mango', 'grape']
m, n, b = fruit

print(m) # apple
print(n) # mango
print(b) # grape
