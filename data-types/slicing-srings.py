# You can get a range of characters of the string:
str1 = "Hello, how are you?"
print(str1[0:5]) # Hello

# Slice from the start:
print(str1[:5]) # Hello

# Slice to the end:
print(str1[7:]) # how are you?

# You can also get characters from the end of the string, using negative indexes:
print(str1[-4:-1]) # you



